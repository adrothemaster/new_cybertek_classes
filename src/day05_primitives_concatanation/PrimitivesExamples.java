package day05_primitives_concatanation;

public class PrimitivesExamples {
    public static void main(String[] args) {
        //***** WHOLE NUMBERS/INTEGER TYPES ****
       byte byteValue = 100;
       short shortValue = 32000;
       int intValue = 21235345;
       long longValue = 522345232334234523L;
       //***** DECIMAL NUMBERS/FLOATING POINT TYPES ****
        float floatValue = 2345.55F;
        double doubleValue = 23554323.4334; //2355432.4334D also valid
        //***** CHARACTER TYPE *****
        char charValue = 'a';
        //***** BOOLEAN = true|false *******
        boolean booleanValue = true;

        //PRINT VARIABLE VALUES
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);
    }
}
